# Pide 7 números enteros por teclado y los suma y los multiplica.

num1 = int(input('Introduzca el primer número: '))
num2 = int(input('Introduzca el segundo número: '))
num3 = int(input('Introduzca el tercer número: '))
num4 = int(input('Introduzca el cuarto número: '))
num5 = int(input('Introduzca el quinto número: '))
num6 = int(input('Introduzca el sexto número: '))
num7 = int(input('Introduzca el séptimo número: '))

suma = num1+num2+num3+num4+num5+num6+num7
mult = num1*num2*num3*num4*num5*num6*num7

print(f'La suma de los números es: {suma}')
print(f'La multiplicación de los números es: {mult}')
